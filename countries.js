/** ISO-3166-1 alpha-2 list for hall-of-fame country pickers. */

function flagFromCode(code) {
  if (code === "OTHER") return "🌍";
  return [...String(code).toUpperCase()]
    .map((ch) => String.fromCodePoint(0x1f1e6 - 65 + ch.charCodeAt(0)))
    .join("");
}

