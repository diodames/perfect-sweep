export const COUNTRIES = [
  ...COUNTRY_NAMES
    .map(([code, name, ...aliases]) => ({
      code,
      name,
      flag: flagFromCode(code),
      aliases,
    }))
    .sort((a, b) => a.name.localeCompare(b.name)),
  { code: "OTHER", name: "Other", flag: "🌍", aliases: [] },
];

export const COUNTRY_CODES = new Set(COUNTRIES.map((c) => c.code));

export function isValidCountry(code) {
  return COUNTRY_CODES.has(String(code || "").toUpperCase());
}

export function countryByCode(code) {
  const c = String(code || "").toUpperCase();
  return COUNTRIES.find((x) => x.code === c) || null;
}

/** Case-insensitive match on name, ISO code, or aliases. */
export function countryMatchesQuery(country, query) {
  const q = String(query || "").trim().toLowerCase();
  if (!q) return true;
  if (country.name.toLowerCase().includes(q)) return true;
  if (country.code.toLowerCase().includes(q)) return true;
  return (country.aliases || []).some((a) => a.toLowerCase().includes(q));
}
